print(f"Ticket Price (each): Rs.{ticket_price}")
print(f"Snack Combo: {snack_text}")
print(f"Snack Combo Total: Rs.{snack_total}\n")

print("-----------------------------------------------------------------------------------")
print(f"Total Amount Payable: Rs.{final_bill}")
print("-----------------------------------------------------------------------------------")
print("Thank you for booking with CineZone Cinema!")
